package timeComplexityQuestions;

public class Program2 {
    public static void main(String[] args) {
        int n = 10;
        for (int i = 0; i < n; i++) {
            for (int j = 0; j < n; j++) {
                System.out.println("Value of i " + i);
                System.out.println("Value of j " + j);
            }
        }
    }
}
